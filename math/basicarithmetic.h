#ifndef BASIC_ARITHMETIC_H_
#define BASIC_ARITHMETIC_H_

template<typename N> static inline N _add_n(N x, N y) { return (x + y); }
template<typename N> static inline N _subt_n(N x, N y) { return (x - y); }
template<typename N> static inline N _mult_n(N x, N y) { return (x * y); }
template<typename N> static inline N _divi_n(N x, N y) { return (x / y); }

#endif // BASIC_ARITHMETIC_H_
